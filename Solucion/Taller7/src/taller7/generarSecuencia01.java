public class generarSecuencia01 {
    public static void main(String[] args) {
        int numerador=1;
        int denominador=10;
        while (numerador<=6){
            System.out.println(numerador +"/"+ denominador);
            numerador=numerador+1;
            denominador=denominador+1;
        }

    }
}    