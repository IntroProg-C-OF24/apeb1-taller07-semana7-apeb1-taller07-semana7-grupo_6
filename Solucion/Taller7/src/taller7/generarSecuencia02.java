package Taller7;
public class generarSecuencia02 {
    public static void main(String[] args) {
        int numerador=5;
        int denominador=10;
        while (numerador<=30){
            System.out.println(numerador +"/"+ denominador);
            numerador=numerador+5;
            denominador=denominador+2;
        }
    }
}    